    def list_payment_requests(
        self,
        *,
        user_id: str,
        workspace_id: str,
        role: str,
        status: Optional[str] = None,
        action_filter: Optional[str] = None,
        limit: int = 50,
        offset: int = 0,
    ) -> Dict[str, Any]:
        """List accessible requests without crossing tenant boundaries."""
        action = PaymentAction.LIST.value

        try:
            context = self._validate_task_context(
                user_id,
                workspace_id,
                role,
                action,
            )
            self._check_permission(context, action)

            clean_limit = max(1, min(500, int(limit)))
            clean_offset = max(0, int(offset))
            clean_status = _clean_string(status).lower() or None
            clean_action_filter = (
                _clean_string(action_filter).lower() or None
            )

            privileged_roles = {
                ActorRole.OWNER.value,
                ActorRole.ADMIN.value,
                ActorRole.FINANCE_ADMIN.value,
                ActorRole.FINANCE_MANAGER.value,
            }

            records: List[PaymentRequest] = []

            for request in self.storage.list_by_workspace(
                context["workspace_id"]
            ):
                if (
                    context["role"] not in privileged_roles
                    and request.user_id != context["user_id"]
                ):
                    continue

                request = self._expire_if_needed(request)

                if clean_status and request.status != clean_status:
                    continue

                if (
                    clean_action_filter
                    and request.action != clean_action_filter
                ):
                    continue

                records.append(request)

            records.sort(
                key=lambda item: item.created_at,
                reverse=True,
            )

            total = len(records)
            selected = records[
                clean_offset: clean_offset + clean_limit
            ]

            self._log_audit_event(
                action,
                context,
                success=True,
                metadata={
                    "total": total,
                    "returned": len(selected),
                },
            )

            return self._safe_result(
                "Payment requests listed.",
                data={
                    "requests": [
                        request.to_dict()
                        for request in selected
                    ],
                    "pagination": {
                        "total": total,
                        "limit": clean_limit,
                        "offset": clean_offset,
                        "returned": len(selected),
                    },
                    "payment_executed_by_guard": False,
                },
            )

        except Exception as exc:
            return self._error_result(
                "Could not list payment requests.",
                error=exc,
            )

    # =========================================================================
    # External Execution Reporting
    # =========================================================================

    def report_external_execution(
        self,
        *,
        user_id: str,
        workspace_id: str,
        role: str,
        request_id: str,
        external_reference: str,
        succeeded: bool,
        reported_amount: Any,
        reported_currency: str,
        provider_name: Optional[str] = None,
        evidence_reference: Optional[str] = None,
        failure_reason: Optional[str] = None,
    ) -> Dict[str, Any]:
        """
        Record execution reported by a separate authorized payment service.

        This method does not call that service and cannot execute payment.
        """
        started_at = time.perf_counter()
        action = "report_external_execution"

        try:
            context = self._validate_task_context(
                user_id,
                workspace_id,
                role,
                PaymentAction.STATUS.value,
            )

            if context["role"] not in {
                ActorRole.OWNER.value,
                ActorRole.ADMIN.value,
                ActorRole.FINANCE_ADMIN.value,
            }:
                raise PaymentPermissionError(
                    "Only an authorized finance administrator may report "
                    "external execution."
                )

            request = self._get_request_or_raise(request_id)
            self._assert_workspace_access(request, context)

            if request.status != (
                PaymentRequestStatus
                .APPROVED_FOR_EXTERNAL_EXECUTION
                .value
            ):
                raise PaymentStateError(
                    "External execution can only be reported after all "
                    "required approvals."
                )

            clean_amount = _to_decimal(
                reported_amount,
                "reported_amount",
            )
            clean_currency = self._normalize_currency(
                reported_currency
            )

            if clean_amount != request.amount:
                raise PaymentValidationError(
                    "Reported amount does not match approved amount."
                )

            if clean_currency != request.currency:
                raise PaymentValidationError(
                    "Reported currency does not match approved currency."
                )

            clean_external_reference = self._validate_reference(
                external_reference,
                "external_reference",
            )
            if not clean_external_reference:
                raise PaymentValidationError(
                    "external_reference is required."
                )

            new_status = (
                PaymentRequestStatus.EXECUTION_REPORTED.value
                if succeeded
                else PaymentRequestStatus.FAILED_REPORTED.value
            )

            metadata = {
                **dict(request.metadata),
                "external_execution_report": {
                    "reported_by": context["user_id"],
                    "reported_at": _utc_now_iso(),
                    "succeeded": bool(succeeded),
                    "provider_name": _clean_string(
                        provider_name
                    ) or None,
                    "evidence_reference": _mask_identifier(
                        evidence_reference
                    ),
                    "failure_reason": (
                        _clean_string(failure_reason) or None
                    ),
                },
            }

            with self._mutation_lock:
                updated = self.storage.update(
                    request.request_id,
                    {
                        "status": new_status,
                        "external_reference": (
                            clean_external_reference
                        ),
                        "metadata": metadata,
                        "updated_at": _utc_now_iso(),
                        "version": request.version + 1,
                    },
                )

            if updated is None:
                raise PaymentStateError(
                    "Unable to save external execution report."
                )

            verification_payload = self._prepare_verification_payload(
                action,
                context,
                updated.to_dict(),
                success=True,
            )

            self._emit_agent_event(
                "payment_guard.external_execution_reported",
                context,
                {
                    "request_id": updated.request_id,
                    "status": updated.status,
                    "succeeded": succeeded,
                },
            )
            self._log_audit_event(
                action,
                context,
                request_id=updated.request_id,
                success=True,
                metadata={
                    "status": updated.status,
                    "provider_name": provider_name,
                },
            )

            return self._safe_result(
                "External execution report stored. PaymentGuard did not "
                "perform the transaction.",
                data={
                    "request": updated.to_dict(),
                    "verification_payload": verification_payload,
                    "payment_executed_by_guard": False,
                    "external_execution_reported": True,
                },
                metadata={
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

        except Exception as exc:
            return self._error_result(
                "Could not record external execution report.",
                error=exc,
                metadata={
                    "request_id": request_id,
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

    # =========================================================================
    # Helpers
    # =========================================================================

    def _get_request_or_raise(
        self,
        request_id: Any,
    ) -> PaymentRequest:
        """Retrieve a request after validating its identifier."""
        clean_id = _clean_string(request_id)

        if not clean_id or not REQUEST_ID_PATTERN.fullmatch(clean_id):
            raise PaymentValidationError(
                "request_id is missing or invalid."
            )

        request = self.storage.get(clean_id)
        if request is None:
            raise PaymentValidationError(
                "Payment request was not found."
            )

        return request

    def _ensure_request_approvable(
        self,
        request: PaymentRequest,
    ) -> None:
        """Ensure request is in a state where approval is allowed."""
        request = self._expire_if_needed(request)

        if request.status == PaymentRequestStatus.HELD.value:
            raise PaymentStateError(
                "Held requests require security or finance review before "
                "approval."
            )

        if request.status not in {
            PaymentRequestStatus.PENDING_APPROVAL.value,
        }:
            raise PaymentStateError(
                f"Request status '{request.status}' cannot be approved."
            )

    def _ensure_not_terminal(
        self,
        request: PaymentRequest,
    ) -> None:
        """Reject mutation of terminal requests."""
        terminal = {
            PaymentRequestStatus.REJECTED.value,
            PaymentRequestStatus.CANCELLED.value,
            PaymentRequestStatus.EXPIRED.value,
            PaymentRequestStatus.EXECUTION_REPORTED.value,
            PaymentRequestStatus.FAILED_REPORTED.value,
        }

        if request.status in terminal:
            raise PaymentStateError(
                f"Request status '{request.status}' is terminal."
            )

    def _expire_if_needed(
        self,
        request: PaymentRequest,
    ) -> PaymentRequest:
        """Expire stale requests safely."""
        if request.status in {
            PaymentRequestStatus.REJECTED.value,
            PaymentRequestStatus.CANCELLED.value,
            PaymentRequestStatus.EXPIRED.value,
            PaymentRequestStatus.EXECUTION_REPORTED.value,
            PaymentRequestStatus.FAILED_REPORTED.value,
        }:
            return request

        expires_at = _parse_iso_datetime(request.expires_at)

        if expires_at is None or _utc_now() < expires_at:
            return request

        updated = self.storage.update(
            request.request_id,
            {
                "status": PaymentRequestStatus.EXPIRED.value,
                "decision": PaymentDecision.EXPIRE.value,
                "approval_challenge_hash": None,
                "approval_challenge_expires_at": None,
                "updated_at": _utc_now_iso(),
                "version": request.version + 1,
            },
        )

        return updated or request

    def _record_failed_approval_attempt(
        self,
        *,
        request_id: str,
        workspace_id: str,
    ) -> None:
        """Count failed confirmations and hold after repeated failures."""
        request = self.storage.get(request_id)
        if request is None or request.workspace_id != workspace_id:
            return

        attempts = request.failed_approval_attempts + 1
        updates: Dict[str, Any] = {
            "failed_approval_attempts": attempts,
            "updated_at": _utc_now_iso(),
            "version": request.version + 1,
        }

        if (
            attempts
            >= self.config.maximum_failed_approval_attempts
        ):
            updates.update(
                {
                    "status": PaymentRequestStatus.HELD.value,
                    "decision": PaymentDecision.HOLD.value,
                    "approval_challenge_hash": None,
                    "approval_challenge_expires_at": None,
                    "risk_findings": [
                        *request.risk_findings,
                        RiskFinding(
                            code="APPROVAL_FAILURE_LIMIT",
                            message=(
                                "Too many failed explicit approval attempts."
                            ),
                            score=30,
                            severity=RiskLevel.HIGH.value,
                            blocking=True,
                        ).to_dict(),
                    ],
                }
            )

        self.storage.update(request_id, updates)

    def _validate_reference(
        self,
        value: Any,
        field_name: str,
    ) -> Optional[str]:
        """Validate token/reference fields without accepting credentials."""
        text = _clean_string(value)
        if not text:
            return None

        secret_findings = _contains_payment_secret(
            {field_name: text}
        )
        if secret_findings:
            raise PaymentValidationError(
                f"{field_name} appears to contain prohibited credentials."
            )

        if len(text) > 255:
            raise PaymentValidationError(
                f"{field_name} exceeds 255 characters."
            )

        if not SAFE_ID_PATTERN.fullmatch(text):
            raise PaymentValidationError(
                f"{field_name} has an invalid format."
            )

        return text

    def _validate_optional_id(
        self,
        value: Any,
        field_name: str,
    ) -> Optional[str]:
        """Validate optional William entity identifiers."""
        text = _clean_string(value)
        if not text:
            return None

        if not SAFE_ID_PATTERN.fullmatch(text):
            raise PaymentValidationError(
                f"{field_name} has an invalid format."
            )

        return text

    def _normalize_approval_method(
        self,
        method: Any,
    ) -> str:
        """Validate approval mechanism."""
        normalized = _clean_string(method).lower()
        allowed = {item.value for item in ApprovalMethod}

        if normalized not in allowed:
            raise PaymentApprovalError(
                f"Unsupported approval method: {normalized or 'missing'}."
            )

        return normalized

    def _challenge_material(
        self,
        *,
        request: PaymentRequest,
        challenge: str,
        session_id: Optional[Any],
        device_id: Optional[Any],
    ) -> str:
        """Bind a challenge to request, workspace, amount, and actor context."""
        return "|".join(
            (
                request.request_id,
                request.workspace_id,
                request.user_id,
                request.action,
                format(request.amount, "f"),
                request.currency,
                _clean_string(session_id),
                _clean_string(device_id),
                challenge,
            )
        )

    def _recipient_fingerprint(
        self,
        recipient: PaymentRecipient,
    ) -> str:
        """Create a privacy-preserving duplicate-detection fingerprint."""
        material = "|".join(
            (
                _clean_string(recipient.recipient_id).lower(),
                _clean_string(recipient.recipient_name).lower(),
                _clean_string(recipient.account_reference).lower(),
                _clean_string(recipient.wallet_reference).lower(),
                _clean_string(recipient.merchant_reference).lower(),
            )
        )
        return _hash_text(material)

    def _approval_summary(
        self,
        request: PaymentRequest,
    ) -> Dict[str, Any]:
        """Create a clear human confirmation summary."""
        return {
            "request_id": request.request_id,
            "action": request.action,
            "amount": format(request.amount, "f"),
            "currency": request.currency,
            "purpose": request.purpose,
            "recipient": request.recipient.to_dict(),
            "risk_level": request.risk_level,
            "risk_score": request.risk_score,
            "required_approvals": request.required_approvals,
            "current_approvals": len(
                [
                    approval
                    for approval in request.approvals
                    if approval.approved
                ]
            ),
            "warning": (
                "Approval does not cause PaymentGuard to send money. A "
                "separate authorized execution service is required."
            ),
        }

    def _creation_message(
        self,
        request: PaymentRequest,
    ) -> str:
        """Return a status-specific creation message."""
        if request.status == PaymentRequestStatus.HELD.value:
            return (
                "Payment request created and placed on security hold. "
                "No payment was executed."
            )

        if request.status == PaymentRequestStatus.REJECTED.value:
            return (
                "Payment request was rejected by PaymentGuard. "
                "No payment was executed."
            )

        return (
            "Payment request created and requires explicit approval. "
            "No payment was executed."
        )

    def _next_step(
        self,
        request: PaymentRequest,
    ) -> Dict[str, Any]:
        """Explain the allowed next step."""
        if request.status == PaymentRequestStatus.HELD.value:
            return {
                "action": "security_review",
                "message": (
                    "A finance/security administrator must review the hold."
                ),
            }

        if request.status == (
            PaymentRequestStatus.PENDING_APPROVAL.value
        ):
            return {
                "action": "generate_approval_challenge",
                "message": (
                    "Generate and explicitly confirm a short-lived approval "
                    "challenge."
                ),
            }

        return {
            "action": "none",
            "message": "No automatic financial action is available.",
        }

    def _clamp_score(self, value: Any) -> int:
        """Normalize an external risk score to 0-100."""
        try:
            score = int(float(value))
        except (TypeError, ValueError):
            score = 10
        return max(0, min(100, score))

    def _normalize_risk_level(self, value: Any) -> str:
        """Normalize external risk level."""
        normalized = _clean_string(value).lower()
        allowed = {item.value for item in RiskLevel}
        return (
            normalized
            if normalized in allowed
            else RiskLevel.MEDIUM.value
        )

    def _elapsed_ms(self, started_at: float) -> float:
        """Return elapsed milliseconds."""
        return round(
            (time.perf_counter() - started_at) * 1000,
            3,
        )

    def _call_optional_client_sync(
        self,
        client: Any,
        method_names: Sequence[str],
        payload: Mapping[str, Any],
    ) -> Any:
        """
        Call a synchronous optional integration safely.

        Async-only clients are not run inside this synchronous method because
        silently creating nested event loops can be unsafe. The generic async
        run path can be extended later with native async integrations.
        """
        for method_name in method_names:
            method = getattr(client, method_name, None)
            if method is None or not callable(method):
                continue

            result = method(copy.deepcopy(dict(payload)))

            if inspect.isawaitable(result):
                if inspect.iscoroutine(result):
                    result.close()
                raise PaymentGuardError(
                    f"Optional client method '{method_name}' is async; "
                    "configure a synchronous adapter for PaymentGuard."
                )

            return result

        raise PaymentGuardError(
            "Optional client does not expose a supported method."
        )

    # =========================================================================
    # Master Agent / Router Entry Point
    # =========================================================================

    async def run(
        self,
        task: Optional[Mapping[str, Any]] = None,
        **kwargs: Any,
    ) -> Dict[str, Any]:
        """
        Generic Master Agent / Agent Router entry point.

        Expected shape:
            {
                "action": "create|assess|challenge|approve|reject|cancel|
                           expire|status|list|validate|report_execution",
                "user_id": "...",
                "workspace_id": "...",
                "role": "...",
                "payload": {...}
            }

        No route in this method executes payment.
        """
        started_at = time.perf_counter()
        incoming: Dict[str, Any] = dict(task or {})
        incoming.update(kwargs)

        route_action = _clean_string(
            incoming.get("action")
        ).lower()
        payload = dict(incoming.get("payload") or {})

        user_id = incoming.get("user_id", payload.get("user_id"))
        workspace_id = incoming.get(
            "workspace_id",
            payload.get("workspace_id"),
        )
        role = incoming.get(
            "role",
            payload.get("role", ActorRole.MEMBER.value),
        )

        try:
            if route_action in {
                "create",
                "create_payment_request",
                "propose_payment",
            }:
                result = self.create_payment_request(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    action=payload.get("financial_action")
                    or payload.get("payment_action")
                    or payload.get("action"),
                    amount=payload.get("amount"),
                    currency=payload.get("currency"),
                    purpose=payload.get("purpose"),
                    recipient=payload.get("recipient"),
                    idempotency_key=payload.get("idempotency_key"),
                    payment_method_reference=payload.get(
                        "payment_method_reference"
                    ),
                    source_account_reference=payload.get(
                        "source_account_reference"
                    ),
                    destination_reference=payload.get(
                        "destination_reference"
                    ),
                    merchant_reference=payload.get(
                        "merchant_reference"
                    ),
                    invoice_reference=payload.get(
                        "invoice_reference"
                    ),
                    subscription_reference=payload.get(
                        "subscription_reference"
                    ),
                    project_id=payload.get("project_id"),
                    client_id=payload.get("client_id"),
                    task_id=payload.get("task_id"),
                    device_id=payload.get("device_id"),
                    session_id=payload.get("session_id"),
                    ip_address=payload.get("ip_address"),
                    country_code=payload.get("country_code"),
                    metadata=payload.get("metadata"),
                )

            elif route_action in {"assess", "review", "reassess"}:
                result = self.assess_payment_request(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    request_id=payload.get("request_id"),
                    session_id=payload.get("session_id"),
                    device_id=payload.get("device_id"),
                )

            elif route_action in {
                "challenge",
                "generate_approval_challenge",
            }:
                result = self.generate_approval_challenge(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    request_id=payload.get("request_id"),
                    session_id=payload.get("session_id"),
                    device_id=payload.get("device_id"),
                )

            elif route_action in {
                "approve",
                "approve_payment_request",
            }:
                result = self.approve_payment_request(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    request_id=payload.get("request_id"),
                    approval_method=payload.get(
                        "approval_method",
                        ApprovalMethod.TYPED_CONFIRMATION.value,
                    ),
                    explicit_confirmation=payload.get(
                        "explicit_confirmation"
                    ),
                    reason=payload.get("reason", ""),
                    biometric_assertion=payload.get(
                        "biometric_assertion"
                    ),
                    evidence_reference=payload.get(
                        "evidence_reference"
                    ),
                    session_id=payload.get("session_id"),
                    device_id=payload.get("device_id"),
                )

            elif route_action in {
                "reject",
                "reject_payment_request",
            }:
                result = self.reject_payment_request(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    request_id=payload.get("request_id"),
                    reason=payload.get("reason", ""),
                    session_id=payload.get("session_id"),
                    device_id=payload.get("device_id"),
                )

            elif route_action in {
                "cancel",
                "cancel_payment_request",
            }:
                result = self.cancel_payment_request(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    request_id=payload.get("request_id"),
                    reason=payload.get("reason", ""),
                    session_id=payload.get("session_id"),
                    device_id=payload.get("device_id"),
                )

            elif route_action in {
                "expire",
                "expire_payment_request",
            }:
                result = self.expire_payment_request(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    request_id=payload.get("request_id"),
                    reason=payload.get(
                        "reason",
                        "Payment request expired.",
                    ),
                )

            elif route_action in {
                "status",
                "get",
                "get_payment_request",
            }:
                result = self.get_payment_request(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    request_id=payload.get("request_id"),
                )

            elif route_action in {
                "list",
                "list_payment_requests",
            }:
                result = self.list_payment_requests(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    status=payload.get("status"),
                    action_filter=payload.get("action_filter"),
                    limit=payload.get("limit", 50),
                    offset=payload.get("offset", 0),
                )

            elif route_action in {
                "validate",
                "validate_payment_payload",
            }:
                result = self.validate_payment_payload(payload)

            elif route_action in {
                "report_execution",
                "report_external_execution",
            }:
                result = self.report_external_execution(
                    user_id=user_id,
                    workspace_id=workspace_id,
                    role=role,
                    request_id=payload.get("request_id"),
                    external_reference=payload.get(
                        "external_reference"
                    ),
                    succeeded=bool(payload.get("succeeded")),
                    reported_amount=payload.get(
                        "reported_amount"
                    ),
                    reported_currency=payload.get(
                        "reported_currency"
                    ),
                    provider_name=payload.get("provider_name"),
                    evidence_reference=payload.get(
                        "evidence_reference"
                    ),
                    failure_reason=payload.get(
                        "failure_reason"
                    ),
                )

            elif route_action in {
                "execute",
                "pay",
                "send_money",
                "complete_purchase",
                "submit_payment",
                "confirm_transfer",
            }:
                result = self._error_result(
                    "PaymentGuard never executes financial transactions. "
                    "Create a protected request, collect explicit approval, "
                    "then use a separately authorized external execution "
                    "service.",
                    error="PAYMENT_EXECUTION_PROHIBITED",
                    metadata={
                        "requested_action": route_action,
                        "payment_executed": False,
                    },
                )

            else:
                result = self._error_result(
                    "Unsupported PaymentGuard action.",
                    error="UNSUPPORTED_ACTION",
                    data={
                        "supported_actions": [
                            "create",
                            "assess",
                            "challenge",
                            "approve",
                            "reject",
                            "cancel",
                            "expire",
                            "status",
                            "list",
                            "validate",
                            "report_execution",
                        ],
                        "prohibited_actions": [
                            "execute",
                            "pay",
                            "send_money",
                            "submit_payment",
                        ],
                    },
                    metadata={
                        "requested_action": route_action,
                    },
                )

            result.setdefault("metadata", {})
            result["metadata"]["runtime_ms"] = self._elapsed_ms(
                started_at
            )
            result["metadata"]["payment_executed_by_guard"] = False
            return result

        except Exception as exc:
            logger.exception("PaymentGuard router execution failed.")
            return self._error_result(
                "PaymentGuard task failed safely.",
                error=exc,
                metadata={
                    "requested_action": route_action,
                    "runtime_ms": self._elapsed_ms(started_at),
                    "payment_executed": False,
                },
            )


