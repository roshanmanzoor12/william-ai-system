    def validate_payment_payload(
        self,
        payload: Mapping[str, Any],
    ) -> Dict[str, Any]:
        """
        Validate a proposed financial payload without storing a request.

        This method never executes or authorizes payment.
        """
        try:
            if not isinstance(payload, Mapping):
                raise PaymentValidationError(
                    "Payment payload must be a dictionary-like object."
                )

            secret_findings = _contains_payment_secret(payload)
            if (
                secret_findings
                and self.config.reject_raw_payment_credentials
            ):
                return self._error_result(
                    "Raw payment credentials are prohibited.",
                    error="PROHIBITED_PAYMENT_CREDENTIALS",
                    data={
                        "secret_types_detected": secret_findings,
                        "accepted_alternatives": sorted(
                            SAFE_PAYMENT_REFERENCE_FIELDS
                        ),
                    },
                    metadata={
                        "validation_only": True,
                        "never_executes_payment": True,
                    },
                )

            action = self._normalize_payment_action(
                payload.get("action")
            )
            amount = _to_decimal(payload.get("amount"), "amount")
            currency = self._normalize_currency(
                payload.get("currency")
            )
            purpose = self._validate_purpose(
                payload.get("purpose", "")
            )
            recipient = self._build_recipient(
                payload.get("recipient")
            )

            self._validate_amount(amount)
            self._validate_recipient_for_action(action, recipient)

            return self._safe_result(
                "Payment payload passed structural validation.",
                data={
                    "action": action,
                    "amount": format(amount, "f"),
                    "currency": currency,
                    "purpose": purpose,
                    "recipient": recipient.to_dict(),
                    "requires_explicit_approval": (
                        action in ALWAYS_APPROVAL_ACTIONS
                    ),
                    "can_execute_payment": False,
                },
                metadata={
                    "validation_only": True,
                },
            )

        except Exception as exc:
            return self._error_result(
                "Payment payload validation failed.",
                error=exc,
                metadata={
                    "validation_only": True,
                },
            )

    def _normalize_payment_action(self, action: Any) -> str:
        """Validate and normalize a payment action."""
        normalized = _clean_string(action).lower()

        aliases = {
            "buy": PaymentAction.PURCHASE.value,
            "checkout": PaymentAction.PURCHASE.value,
            "pay": PaymentAction.PAYMENT.value,
            "send_money": PaymentAction.TRANSFER.value,
            "send": PaymentAction.TRANSFER.value,
            "bank": PaymentAction.BANK_TRANSFER.value,
            "wire": PaymentAction.WIRE_TRANSFER.value,
            "card": PaymentAction.CARD_PAYMENT.value,
            "wallet": PaymentAction.WALLET_PAYMENT.value,
            "subscribe": PaymentAction.SUBSCRIPTION_CREATE.value,
            "withdraw": PaymentAction.WITHDRAWAL.value,
            "exchange": PaymentAction.CURRENCY_EXCHANGE.value,
            "crypto": PaymentAction.CRYPTO_TRANSFER.value,
        }

        normalized = aliases.get(normalized, normalized)

        allowed = {
            item.value
            for item in PaymentAction
            if item not in {
                PaymentAction.APPROVE,
                PaymentAction.REJECT,
                PaymentAction.CANCEL,
                PaymentAction.EXPIRE,
                PaymentAction.STATUS,
                PaymentAction.LIST,
                PaymentAction.VALIDATE,
                PaymentAction.REVIEW,
            }
        }

        if normalized not in allowed:
            raise PaymentValidationError(
                f"Unsupported financial action: {normalized or 'missing'}."
            )

        return normalized

    def _normalize_currency(self, currency: Any) -> str:
        """Validate ISO-like currency or token symbol."""
        normalized = _clean_string(currency).upper()

        if not normalized:
            raise PaymentValidationError("currency is required.")

        if not SUPPORTED_CURRENCY_PATTERN.fullmatch(normalized):
            raise PaymentValidationError(
                "currency must contain 3 to 10 uppercase letters."
            )

        return normalized

    def _validate_amount(self, amount: Decimal) -> None:
        """Enforce positive finite amount and global hard limit."""
        if amount <= ZERO_MONEY:
            raise PaymentValidationError(
                "amount must be greater than zero."
            )

        if amount > self.config.max_single_amount:
            raise PaymentValidationError(
                "amount exceeds the PaymentGuard global maximum."
            )

    def _validate_purpose(self, purpose: Any) -> str:
        """Validate payment purpose and detect prohibited intent."""
        normalized = " ".join(_clean_string(purpose).split())

        if not normalized:
            raise PaymentValidationError(
                "A clear payment purpose is required."
            )

        if len(normalized) > 1000:
            raise PaymentValidationError(
                "Payment purpose exceeds 1000 characters."
            )

        lowered = normalized.lower()

        if (
            self.config.reject_blocked_purpose_terms
            and any(term in lowered for term in BLOCKED_PURPOSE_TERMS)
        ):
            raise PaymentValidationError(
                "Payment purpose contains a prohibited or illegal-use signal."
            )

        return normalized

    def _build_recipient(self, value: Any) -> PaymentRecipient:
        """Build a recipient without accepting account credentials."""
        if value is None:
            return PaymentRecipient()

        if isinstance(value, PaymentRecipient):
            secret_findings = _contains_payment_secret(value.metadata)
            if secret_findings:
                raise PaymentValidationError(
                    "Recipient metadata contains prohibited credentials."
                )
            return copy.deepcopy(value)

        if not isinstance(value, Mapping):
            raise PaymentValidationError(
                "recipient must be a dictionary-like object."
            )

        secret_findings = _contains_payment_secret(value)
        if secret_findings:
            raise PaymentValidationError(
                "Recipient data contains prohibited financial credentials."
            )

        country_code = (
            _clean_string(value.get("country_code")).upper() or None
        )
        if country_code and not re.fullmatch(r"^[A-Z]{2,3}$", country_code):
            raise PaymentValidationError(
                "recipient country_code is invalid."
            )

        return PaymentRecipient(
            recipient_id=_clean_string(
                value.get("recipient_id")
            ) or None,
            recipient_name=_clean_string(
                value.get("recipient_name")
                or value.get("name")
            ) or None,
            recipient_type=_clean_string(
                value.get("recipient_type"),
                "unknown",
            ).lower(),
            country_code=country_code,
            account_reference=_clean_string(
                value.get("account_reference")
                or value.get("bank_account_token")
            ) or None,
            wallet_reference=_clean_string(
                value.get("wallet_reference")
                or value.get("wallet_id")
            ) or None,
            merchant_reference=_clean_string(
                value.get("merchant_reference")
            ) or None,
            is_new_recipient=bool(
                value.get("is_new_recipient", False)
            ),
            is_verified=bool(value.get("is_verified", False)),
            metadata=_redact_sensitive_data(
                dict(value.get("metadata") or {})
            ),
        )

    def _validate_recipient_for_action(
        self,
        action: str,
        recipient: PaymentRecipient,
    ) -> None:
        """Require recipient identity for value-moving operations."""
        requires_recipient = action in {
            PaymentAction.PURCHASE.value,
            PaymentAction.PAYMENT.value,
            PaymentAction.TRANSFER.value,
            PaymentAction.BANK_TRANSFER.value,
            PaymentAction.WIRE_TRANSFER.value,
            PaymentAction.CARD_PAYMENT.value,
            PaymentAction.WALLET_PAYMENT.value,
            PaymentAction.WITHDRAWAL.value,
            PaymentAction.PAYOUT.value,
            PaymentAction.INVOICE_PAYMENT.value,
            PaymentAction.CRYPTO_TRANSFER.value,
            PaymentAction.DONATION.value,
            PaymentAction.FEE_PAYMENT.value,
        }

        has_reference = any(
            (
                recipient.recipient_id,
                recipient.recipient_name,
                recipient.account_reference,
                recipient.wallet_reference,
                recipient.merchant_reference,
            )
        )

        if (
            requires_recipient
            and self.config.require_recipient_for_transfer
            and not has_reference
        ):
            raise PaymentValidationError(
                "A recipient or merchant reference is required."
            )

