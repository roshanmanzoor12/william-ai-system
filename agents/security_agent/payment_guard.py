    def _change_terminal_state(
        self,
        *,
        user_id: str,
        workspace_id: str,
        role: str,
        request_id: str,
        reason: str,
        action: str,
        status: str,
        decision: str,
        timestamp_field: Optional[str],
        session_id: Optional[str],
        device_id: Optional[str],
    ) -> Dict[str, Any]:
        """Apply a safe rejection/cancellation/expiration transition."""
        started_at = time.perf_counter()

        try:
            context = self._validate_task_context(
                user_id,
                workspace_id,
                role,
                action,
                session_id=session_id,
                device_id=device_id,
            )
            self._check_permission(context, action)

            request = self._get_request_or_raise(request_id)
            self._assert_workspace_access(request, context)

            if request.status == (
                PaymentRequestStatus.EXECUTION_REPORTED.value
            ):
                raise PaymentStateError(
                    "A reported completed external payment cannot be "
                    "cancelled by PaymentGuard."
                )

            clean_reason = " ".join(_clean_string(reason).split())
            if len(clean_reason) < 3:
                raise PaymentValidationError(
                    "A clear reason is required."
                )

            updates: Dict[str, Any] = {
                "status": status,
                "decision": decision,
                "approval_challenge_hash": None,
                "approval_challenge_expires_at": None,
                "updated_at": _utc_now_iso(),
                "version": request.version + 1,
                "metadata": {
                    **dict(request.metadata),
                    f"{action}_reason": clean_reason,
                    f"{action}_by": context["user_id"],
                },
            }

            if timestamp_field:
                updates[timestamp_field] = _utc_now_iso()

            with self._mutation_lock:
                updated = self.storage.update(
                    request.request_id,
                    updates,
                )

            if updated is None:
                raise PaymentStateError(
                    "Unable to update payment request."
                )

            verification_payload = self._prepare_verification_payload(
                action,
                context,
                updated.to_dict(),
                success=True,
            )
            memory_payload = self._prepare_memory_payload(
                updated,
                f"payment_request_{action}",
            )

            self._emit_agent_event(
                f"payment_guard.request_{action}",
                context,
                {
                    "request_id": updated.request_id,
                    "status": updated.status,
                },
            )
            self._log_audit_event(
                action,
                context,
                request_id=updated.request_id,
                success=True,
                metadata={
                    "reason": clean_reason,
                    "status": updated.status,
                },
            )

            return self._safe_result(
                f"Payment request {action} completed safely. No payment "
                "was executed.",
                data={
                    "request": updated.to_dict(),
                    "verification_payload": verification_payload,
                    "memory_payload": memory_payload,
                    "payment_executed": False,
                },
                metadata={
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

        except Exception as exc:
            return self._error_result(
                f"Could not {action} payment request.",
                error=exc,
                metadata={
                    "request_id": request_id,
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

