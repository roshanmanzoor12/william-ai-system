    def generate_approval_challenge(
        self,
        *,
        user_id: str,
        workspace_id: str,
        role: str,
        request_id: str,
        session_id: Optional[str] = None,
        device_id: Optional[str] = None,
    ) -> Dict[str, Any]:
        """
        Generate a short-lived typed approval challenge.

        The challenge must be explicitly returned by the user for approval.
        """
        started_at = time.perf_counter()
        action = PaymentAction.APPROVE.value

        try:
            context = self._validate_task_context(
                user_id,
                workspace_id,
                role,
                action,
                session_id=session_id,
                device_id=device_id,
            )
            self._check_permission(context, action)

            request = self._get_request_or_raise(request_id)
            self._assert_workspace_access(request, context)
            self._ensure_request_approvable(request)

            nonce = secrets.token_hex(3).upper()
            challenge = (
                f"{self.config.typed_confirmation_prefix} "
                f"{request.currency} {format(request.amount, 'f')} "
                f"{request.request_id[-8:]} {nonce}"
            )
            challenge_hash = _hash_text(
                self._challenge_material(
                    request=request,
                    challenge=challenge,
                    session_id=context.get("session_id"),
                    device_id=context.get("device_id"),
                )
            )
            expires_at = (
                _utc_now()
                + timedelta(
                    seconds=self.config.approval_ttl_seconds
                )
            ).isoformat()

            with self._mutation_lock:
                updated = self.storage.update(
                    request.request_id,
                    {
                        "approval_challenge_hash": challenge_hash,
                        "approval_challenge_expires_at": expires_at,
                        "failed_approval_attempts": 0,
                        "updated_at": _utc_now_iso(),
                        "version": request.version + 1,
                    },
                )

            if updated is None:
                raise PaymentStateError(
                    "Unable to save approval challenge."
                )

            self._emit_agent_event(
                "payment_guard.approval_challenge_created",
                context,
                {
                    "request_id": request.request_id,
                    "expires_at": expires_at,
                },
            )
            self._log_audit_event(
                action,
                context,
                request_id=request.request_id,
                success=True,
                metadata={
                    "event": "approval_challenge_created",
                    "expires_at": expires_at,
                },
            )

            return self._safe_result(
                "Explicit approval challenge created. Returning the exact "
                "challenge does not execute payment; it only records approval.",
                data={
                    "request_id": request.request_id,
                    "challenge": challenge,
                    "expires_at": expires_at,
                    "approval_summary": self._approval_summary(request),
                    "payment_executed": False,
                },
                metadata={
                    "runtime_ms": self._elapsed_ms(started_at),
                    "challenge_is_single_use": True,
                },
            )

        except Exception as exc:
            return self._error_result(
                "Could not create payment approval challenge.",
                error=exc,
                metadata={
                    "request_id": request_id,
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

    def approve_payment_request(
        self,
        *,
        user_id: str,
        workspace_id: str,
        role: str,
        request_id: str,
        approval_method: str,
        explicit_confirmation: Optional[str] = None,
        reason: str = "",
        biometric_assertion: Optional[Mapping[str, Any]] = None,
        evidence_reference: Optional[str] = None,
        session_id: Optional[str] = None,
        device_id: Optional[str] = None,
    ) -> Dict[str, Any]:
        """
        Record explicit approval.

        Even after all approvals, status becomes
        APPROVED_FOR_EXTERNAL_EXECUTION. PaymentGuard does not execute payment.
        """
        started_at = time.perf_counter()
        action = PaymentAction.APPROVE.value

        try:
            context = self._validate_task_context(
                user_id,
                workspace_id,
                role,
                action,
                session_id=session_id,
                device_id=device_id,
            )
            self._check_permission(context, action)

            request = self._get_request_or_raise(request_id)
            self._assert_workspace_access(request, context)
            self._ensure_request_approvable(request)

            clean_method = self._normalize_approval_method(
                approval_method
            )
            clean_reason = " ".join(_clean_string(reason).split())

            if (
                len(clean_reason)
                < self.config.minimum_approval_reason_length
            ):
                raise PaymentApprovalError(
                    "An approval reason is required."
                )

            self._enforce_approver_separation(request, context)

            if clean_method == ApprovalMethod.TYPED_CONFIRMATION.value:
                self._validate_typed_confirmation(
                    request=request,
                    confirmation=explicit_confirmation,
                    context=context,
                )

            elif clean_method == ApprovalMethod.BIOMETRIC.value:
                self._validate_biometric_assertion(
                    request=request,
                    context=context,
                    assertion=biometric_assertion,
                )

            elif clean_method in {
                ApprovalMethod.PASSKEY.value,
                ApprovalMethod.HARDWARE_KEY.value,
                ApprovalMethod.OUT_OF_BAND.value,
            }:
                if not evidence_reference:
                    raise PaymentApprovalError(
                        "Evidence reference is required for this approval method."
                    )

            elif clean_method in {
                ApprovalMethod.ADMIN_APPROVAL.value,
                ApprovalMethod.MULTI_PARTY_APPROVAL.value,
            }:
                if context["role"] not in {
                    ActorRole.OWNER.value,
                    ActorRole.ADMIN.value,
                    ActorRole.FINANCE_ADMIN.value,
                    ActorRole.FINANCE_MANAGER.value,
                }:
                    raise PaymentPermissionError(
                        "This approval method requires a finance-authorized role."
                    )

            security_review = self._request_security_approval(
                action,
                context,
                {
                    "request": request.to_dict(),
                    "approval_method": clean_method,
                    "approval_reason": clean_reason,
                },
            )
            if not security_review.get("approved", False):
                raise PaymentApprovalError(
                    _clean_string(
                        security_review.get("reason"),
                        "Security Agent denied approval.",
                    )
                )

            existing_approvers = {
                approval.approver_user_id
                for approval in request.approvals
                if approval.approved
            }
            if context["user_id"] in existing_approvers:
                raise PaymentApprovalError(
                    "The same user cannot approve the request twice."
                )

            approval = ApprovalRecord(
                approval_id=_new_id("payment_approval"),
                approver_user_id=context["user_id"],
                approver_role=context["role"],
                method=clean_method,
                approved=True,
                reason=clean_reason,
                approved_at=_utc_now_iso(),
                device_id=context.get("device_id"),
                session_id=context.get("session_id"),
                evidence_reference=self._validate_reference(
                    evidence_reference,
                    "evidence_reference",
                ),
            )

            approvals = list(request.approvals)
            approvals.append(approval)

            approval_count = len(
                [item for item in approvals if item.approved]
            )

            if approval_count >= request.required_approvals:
                new_status = (
                    PaymentRequestStatus
                    .APPROVED_FOR_EXTERNAL_EXECUTION
                    .value
                )
                new_decision = (
                    PaymentDecision
                    .APPROVED_FOR_EXTERNAL_EXECUTION
                    .value
                )
                approved_at = _utc_now_iso()
                message = (
                    "Required approvals recorded. The request is approved "
                    "for a separate external execution layer. PaymentGuard "
                    "did not execute payment."
                )
            else:
                new_status = (
                    PaymentRequestStatus.PENDING_APPROVAL.value
                )
                new_decision = (
                    PaymentDecision.REQUIRE_APPROVAL.value
                )
                approved_at = None
                message = (
                    "Approval recorded. Additional independent approval is "
                    "still required. No payment was executed."
                )

            with self._mutation_lock:
                updated = self.storage.update(
                    request.request_id,
                    {
                        "approvals": approvals,
                        "status": new_status,
                        "decision": new_decision,
                        "approved_at": approved_at,
                        "approval_challenge_hash": None,
                        "approval_challenge_expires_at": None,
                        "failed_approval_attempts": 0,
                        "updated_at": _utc_now_iso(),
                        "version": request.version + 1,
                    },
                )

            if updated is None:
                raise PaymentStateError(
                    "Unable to save payment approval."
                )

            verification_payload = self._prepare_verification_payload(
                action,
                context,
                updated.to_dict(),
                success=True,
            )
            memory_payload = self._prepare_memory_payload(
                updated,
                "payment_request_approved",
            )

            self._emit_agent_event(
                "payment_guard.approval_recorded",
                context,
                {
                    "request_id": updated.request_id,
                    "approval_count": approval_count,
                    "required_approvals": (
                        updated.required_approvals
                    ),
                    "status": updated.status,
                },
            )
            self._log_audit_event(
                action,
                context,
                request_id=updated.request_id,
                success=True,
                metadata={
                    "approval_method": clean_method,
                    "approval_count": approval_count,
                    "required_approvals": (
                        updated.required_approvals
                    ),
                    "status": updated.status,
                },
            )

            return self._safe_result(
                message,
                data={
                    "request": updated.to_dict(),
                    "approval": approval.to_dict(),
                    "security_review": _redact_sensitive_data(
                        security_review
                    ),
                    "verification_payload": verification_payload,
                    "memory_payload": memory_payload,
                    "payment_executed": False,
                    "external_execution_permitted": (
                        updated.status
                        == PaymentRequestStatus
                        .APPROVED_FOR_EXTERNAL_EXECUTION
                        .value
                    ),
                },
                metadata={
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

        except Exception as exc:
            logger.exception("Payment approval failed.")

            try:
                self._record_failed_approval_attempt(
                    request_id=request_id,
                    workspace_id=workspace_id,
                )
            except Exception:
                logger.exception(
                    "Could not record failed payment approval attempt."
                )

            return self._error_result(
                "Payment approval was not recorded.",
                error=exc,
                metadata={
                    "request_id": request_id,
                    "runtime_ms": self._elapsed_ms(started_at),
                    "payment_executed": False,
                },
            )

    def _validate_typed_confirmation(
        self,
        *,
        request: PaymentRequest,
        confirmation: Optional[str],
        context: Mapping[str, Any],
    ) -> None:
        """Validate single-use, short-lived explicit typed confirmation."""
        if not confirmation:
            raise PaymentApprovalError(
                "Explicit typed confirmation is required."
            )

        if not request.approval_challenge_hash:
            raise PaymentApprovalError(
                "Generate an approval challenge before approving."
            )

        expires_at = _parse_iso_datetime(
            request.approval_challenge_expires_at
        )
        if expires_at is None or _utc_now() >= expires_at:
            raise PaymentApprovalError(
                "Approval challenge has expired."
            )

        candidate_hash = _hash_text(
            self._challenge_material(
                request=request,
                challenge=_clean_string(confirmation),
                session_id=context.get("session_id"),
                device_id=context.get("device_id"),
            )
        )

        if not _constant_time_equal(
            candidate_hash,
            request.approval_challenge_hash,
        ):
            raise PaymentApprovalError(
                "Explicit confirmation does not match the challenge."
            )

    def _validate_biometric_assertion(
        self,
        *,
        request: PaymentRequest,
        context: Mapping[str, Any],
        assertion: Optional[Mapping[str, Any]],
    ) -> None:
        """Validate biometric approval through an optional biometric gate."""
        if self.biometric_client is None:
            raise PaymentApprovalError(
                "Biometric approval service is not configured."
            )

        if not assertion:
            raise PaymentApprovalError(
                "Biometric assertion is required."
            )

        payload = {
            "user_id": context.get("user_id"),
            "workspace_id": context.get("workspace_id"),
            "request_id": request.request_id,
            "amount": format(request.amount, "f"),
            "currency": request.currency,
            "assertion": _redact_sensitive_data(dict(assertion)),
        }

        response = self._call_optional_client_sync(
            self.biometric_client,
            (
                "verify_payment",
                "verify_assertion",
                "verify",
                "check",
            ),
            payload,
        )

        if isinstance(response, Mapping):
            verified = bool(
                response.get(
                    "verified",
                    response.get("approved", False),
                )
            )
        else:
            verified = bool(response)

        if not verified:
            raise PaymentApprovalError(
                "Biometric approval could not be verified."
            )

    def _enforce_approver_separation(
        self,
        request: PaymentRequest,
        context: Mapping[str, Any],
    ) -> None:
        """Enforce maker-checker controls for elevated requests."""
        same_user = request.user_id == context.get("user_id")
        role = _clean_string(context.get("role")).lower()

        if not same_user:
            return

        low_risk_owner_exception = (
            self.config.allow_owner_self_approval_below_threshold
            and role
            in {
                ActorRole.OWNER.value,
                ActorRole.FINANCE_ADMIN.value,
            }
            and request.amount
            <= self.config.owner_self_approval_threshold
            and request.risk_level
            in {
                RiskLevel.LOW.value,
                RiskLevel.MEDIUM.value,
            }
            and request.required_approvals == 1
        )

        if low_risk_owner_exception:
            return

        if (
            request.required_approvals > 1
            or request.risk_level
            in {
                RiskLevel.HIGH.value,
                RiskLevel.CRITICAL.value,
            }
        ):
            raise PaymentApprovalError(
                "An independent finance-authorized approver is required."
            )

