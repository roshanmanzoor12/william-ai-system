    def _requires_security_check(
        self,
        action: str,
        payload: Optional[Mapping[str, Any]] = None,
    ) -> bool:
        """All value-moving financial actions require security review."""
        normalized = _clean_string(action).lower()
        payload = payload or {}

        if normalized in ALWAYS_APPROVAL_ACTIONS:
            return True

        if normalized in {
            PaymentAction.APPROVE.value,
            PaymentAction.REJECT.value,
            PaymentAction.CANCEL.value,
        }:
            return True

        try:
            amount = _to_decimal(payload.get("amount", "0"))
        except PaymentValidationError:
            return True

        if amount > ZERO_MONEY:
            return True

        if _contains_payment_secret(payload):
            return True

        return False

    def _request_security_approval(
        self,
        action: str,
        context: Mapping[str, Any],
        payload: Optional[Mapping[str, Any]] = None,
    ) -> Dict[str, Any]:
        """
        Ask the parent Security Agent for a safety decision.

        This is not human payment authorization and cannot replace explicit
        per-request approval.
        """
        request_payload = {
            "security_request_id": _new_id("payment_security"),
            "request_type": "financial_safety_review",
            "agent": self.agent_name,
            "action": action,
            "context": {
                "user_id": context.get("user_id"),
                "workspace_id": context.get("workspace_id"),
                "role": context.get("role"),
                "session_id": _mask_identifier(
                    _clean_string(context.get("session_id"))
                ),
                "device_id": _mask_identifier(
                    _clean_string(context.get("device_id"))
                ),
            },
            "payload": _redact_sensitive_data(dict(payload or {})),
            "created_at": _utc_now_iso(),
            "notice": (
                "Security approval validates safety only and does not authorize "
                "or execute payment."
            ),
        }

        if self.security_client is None:
            return {
                "approved": True,
                "reason": (
                    "No parent Security Agent client is configured. Local "
                    "PaymentGuard policy remains enforced."
                ),
                "fallback": True,
                "request": request_payload,
            }

        try:
            response = self._call_optional_client_sync(
                self.security_client,
                (
                    "review_payment",
                    "request_approval",
                    "approve",
                    "evaluate",
                    "check",
                ),
                request_payload,
            )
        except Exception as exc:
            logger.exception("Security Agent payment review failed.")
            return {
                "approved": False,
                "reason": f"Security review failed safely: {exc}",
                "fallback": False,
                "request": request_payload,
            }

        if isinstance(response, Mapping):
            approved = bool(
                response.get(
                    "approved",
                    response.get("allowed", False),
                )
            )
            reason = _clean_string(
                response.get("reason")
                or response.get("message")
                or "Security Agent returned a decision."
            )
        else:
            approved = bool(response)
            reason = "Security Agent returned a boolean decision."

        return {
            "approved": approved,
            "reason": reason,
            "fallback": False,
            "request": request_payload,
            "response": _redact_sensitive_data(response),
        }

