def _utc_now() -> datetime:
    """Return timezone-aware UTC now."""
    return datetime.now(timezone.utc)


def _utc_now_iso() -> str:
    """Return timezone-aware UTC timestamp."""
    return _utc_now().isoformat()


def _new_id(prefix: str) -> str:
    """Generate a non-sequential opaque identifier."""
    return f"{prefix}_{uuid.uuid4().hex}"


def _clean_string(value: Any, default: str = "") -> str:
    """Convert a value to a stripped string."""
    if value is None:
        return default
    return str(value).strip()


def _normalize_string_list(value: Any) -> List[str]:
    """Normalize strings or iterables into a deduplicated string list."""
    if value is None:
        return []

    if isinstance(value, str):
        values: Iterable[Any] = [value]
    elif isinstance(value, Iterable):
        values = value
    else:
        values = [value]

    output: List[str] = []
    seen: Set[str] = set()

    for item in values:
        text = _clean_string(item)
        if not text:
            continue
        if text not in seen:
            seen.add(text)
            output.append(text)

    return output


def _json_safe(value: Any) -> Any:
    """Convert arbitrary values into JSON-compatible data."""
    if value is None or isinstance(value, (str, int, float, bool)):
        return value

    if isinstance(value, Decimal):
        return format(value, "f")

    if isinstance(value, datetime):
        return value.isoformat()

    if isinstance(value, Enum):
        return value.value

    if isinstance(value, Mapping):
        return {
            str(key): _json_safe(item)
            for key, item in value.items()
        }

    if isinstance(value, (list, tuple, set, frozenset)):
        return [_json_safe(item) for item in value]

    if hasattr(value, "to_dict") and callable(value.to_dict):
        return _json_safe(value.to_dict())

    if hasattr(value, "__dataclass_fields__"):
        return _json_safe(asdict(value))

    try:
        json.dumps(value)
        return value
    except (TypeError, ValueError):
        return str(value)


def _parse_iso_datetime(value: Optional[str]) -> Optional[datetime]:
    """Safely parse an ISO datetime and normalize it to UTC."""
    if not value:
        return None

    text = _clean_string(value)
    if not text:
        return None

    try:
        parsed = datetime.fromisoformat(text.replace("Z", "+00:00"))
    except ValueError:
        return None

    if parsed.tzinfo is None:
        parsed = parsed.replace(tzinfo=timezone.utc)

    return parsed.astimezone(timezone.utc)


def _to_decimal(value: Any, field_name: str = "amount") -> Decimal:
    """Parse money into a finite positive Decimal."""
    try:
        amount = Decimal(str(value))
    except (InvalidOperation, TypeError, ValueError) as exc:
        raise PaymentValidationError(
            f"{field_name} must be a valid monetary value."
        ) from exc

    if not amount.is_finite():
        raise PaymentValidationError(f"{field_name} must be finite.")

    return amount.quantize(MONETARY_QUANTUM, rounding=ROUND_HALF_UP)


def _constant_time_equal(left: str, right: str) -> bool:
    """Compare sensitive confirmation values in constant time."""
    return hmac.compare_digest(
        left.encode("utf-8"),
        right.encode("utf-8"),
    )


def _hash_text(value: str) -> str:
    """Create a one-way SHA-256 fingerprint."""
    return hashlib.sha256(value.encode("utf-8")).hexdigest()


def _mask_identifier(value: Optional[str], visible: int = 4) -> Optional[str]:
    """Mask a non-secret identifier while retaining a short suffix."""
    if value is None:
        return None

    text = _clean_string(value)
    if not text:
        return None

    if len(text) <= visible:
        return "*" * len(text)

    return f"{'*' * min(8, len(text) - visible)}{text[-visible:]}"


def _luhn_valid(number: str) -> bool:
    """Return whether a numeric string passes the Luhn checksum."""
    digits = [int(char) for char in number if char.isdigit()]
    if len(digits) < 13 or len(digits) > 19:
        return False

    checksum = 0
    parity = len(digits) % 2

    for index, digit in enumerate(digits):
        value = digit
        if index % 2 == parity:
            value *= 2
            if value > 9:
                value -= 9
        checksum += value

    return checksum % 10 == 0


def _contains_payment_secret(value: Any) -> List[str]:
    """
    Find prohibited secret types without returning the secret itself.

    The returned list contains only secret classifications.
    """
    findings: Set[str] = set()

    def inspect_item(item: Any, parent_key: str = "") -> None:
        normalized_key = parent_key.lower().strip()

        if normalized_key in SECRET_FIELD_NAMES and item not in (None, "", [], {}):
            findings.add(f"prohibited_field:{normalized_key}")

        if isinstance(item, Mapping):
            for key, child in item.items():
                inspect_item(child, str(key))
            return

        if isinstance(item, (list, tuple, set)):
            for child in item:
                inspect_item(child, parent_key)
            return

        if not isinstance(item, str):
            return

        text = item

        if CVV_PATTERN.search(text):
            findings.add("cvv_or_cvc")

        if PIN_PATTERN.search(text):
            findings.add("payment_pin")

        if OTP_PATTERN.search(text):
            findings.add("one_time_password")

        if PRIVATE_KEY_PATTERN.search(text):
            findings.add("private_key")

        if SEED_PHRASE_HINT_PATTERN.search(text):
            findings.add("seed_or_recovery_phrase")

        if PASSWORD_PATTERN.search(text):
            findings.add("password")

        if BANK_LOGIN_PATTERN.search(text):
            findings.add("bank_login")

        for match in FULL_CARD_PATTERN.finditer(text):
            candidate = re.sub(r"\D", "", match.group(0))
            if _luhn_valid(candidate):
                findings.add("full_payment_card_number")
                break

    inspect_item(value)
    return sorted(findings)


def _redact_sensitive_data(value: Any) -> Any:
    """Recursively redact financial credentials and likely secret material."""
    if isinstance(value, Mapping):
        output: Dict[str, Any] = {}

        for key, item in value.items():
            key_text = str(key)
            key_lower = key_text.lower().strip()

            if key_lower in SECRET_FIELD_NAMES:
                output[key_text] = "***REDACTED***"
                continue

            if key_lower in SAFE_PAYMENT_REFERENCE_FIELDS:
                if key_lower == "last4":
                    last4 = re.sub(r"\D", "", _clean_string(item))[-4:]
                    output[key_text] = last4
                else:
                    output[key_text] = _mask_identifier(_clean_string(item))
                continue

            output[key_text] = _redact_sensitive_data(item)

        return output

    if isinstance(value, (list, tuple, set)):
        return [_redact_sensitive_data(item) for item in value]

    if isinstance(value, str):
        text = value

        text = PRIVATE_KEY_PATTERN.sub(
            "***REDACTED_PRIVATE_KEY_HEADER***",
            text,
        )
        text = CVV_PATTERN.sub("***REDACTED_CVV***", text)
        text = PIN_PATTERN.sub("***REDACTED_PIN***", text)
        text = OTP_PATTERN.sub("***REDACTED_OTP***", text)
        text = PASSWORD_PATTERN.sub("***REDACTED_PASSWORD***", text)

        def replace_card(match: re.Match[str]) -> str:
            candidate = re.sub(r"\D", "", match.group(0))
            if _luhn_valid(candidate):
                return f"**** **** **** {candidate[-4:]}"
            return match.group(0)

        return FULL_CARD_PATTERN.sub(replace_card, text)

    return _json_safe(value)


