    def create_payment_request(
        self,
        *,
        user_id: str,
        workspace_id: str,
        role: str,
        action: str,
        amount: Any,
        currency: str,
        purpose: str,
        recipient: Optional[
            Union[PaymentRecipient, Mapping[str, Any]]
        ] = None,
        idempotency_key: Optional[str] = None,
        payment_method_reference: Optional[str] = None,
        source_account_reference: Optional[str] = None,
        destination_reference: Optional[str] = None,
        merchant_reference: Optional[str] = None,
        invoice_reference: Optional[str] = None,
        subscription_reference: Optional[str] = None,
        project_id: Optional[str] = None,
        client_id: Optional[str] = None,
        task_id: Optional[str] = None,
        device_id: Optional[str] = None,
        session_id: Optional[str] = None,
        ip_address: Optional[str] = None,
        country_code: Optional[str] = None,
        metadata: Optional[Mapping[str, Any]] = None,
    ) -> Dict[str, Any]:
        """
        Create and assess a protected financial request.

        The result is pending approval, held, or rejected. It is never executed.
        """
        started_at = time.perf_counter()
        normalized_action = _clean_string(action).lower()
        context: Dict[str, Any] = {}

        try:
            context = self._validate_task_context(
                user_id,
                workspace_id,
                role,
                normalized_action,
                session_id=session_id,
                device_id=device_id,
            )

            normalized_action = self._normalize_payment_action(
                normalized_action
            )
            context["action"] = normalized_action
            self._check_permission(context, normalized_action)

            raw_payload = {
                "action": normalized_action,
                "amount": amount,
                "currency": currency,
                "purpose": purpose,
                "recipient": recipient,
                "metadata": metadata,
                "payment_method_reference": (
                    payment_method_reference
                ),
                "source_account_reference": (
                    source_account_reference
                ),
                "destination_reference": destination_reference,
            }

            secret_findings = _contains_payment_secret(raw_payload)
            if (
                secret_findings
                and self.config.reject_raw_payment_credentials
            ):
                self._log_audit_event(
                    normalized_action,
                    context,
                    success=False,
                    metadata={
                        "reason": "prohibited_credentials",
                        "secret_types": secret_findings,
                    },
                )
                return self._error_result(
                    "Payment request rejected because raw financial "
                    "credentials were provided.",
                    error="PROHIBITED_PAYMENT_CREDENTIALS",
                    data={
                        "secret_types_detected": secret_findings,
                        "use_tokenized_references_only": True,
                    },
                    metadata={
                        "runtime_ms": self._elapsed_ms(started_at),
                    },
                )

            clean_amount = _to_decimal(amount)
            self._validate_amount(clean_amount)
            clean_currency = self._normalize_currency(currency)
            clean_purpose = self._validate_purpose(purpose)
            clean_recipient = self._build_recipient(recipient)
            self._validate_recipient_for_action(
                normalized_action,
                clean_recipient,
            )

            clean_country = _clean_string(country_code).upper() or None
            if (
                clean_country
                and not re.fullmatch(r"^[A-Z]{2,3}$", clean_country)
            ):
                raise PaymentValidationError(
                    "country_code is invalid."
                )

            idempotency_hash: Optional[str] = None
            if idempotency_key:
                clean_key = _clean_string(idempotency_key)
                if len(clean_key) < 8:
                    raise PaymentValidationError(
                        "idempotency_key must contain at least 8 characters."
                    )
                idempotency_hash = _hash_text(
                    f"{workspace_id}:{clean_key}"
                )

                duplicate_by_key = (
                    self.storage.find_by_idempotency_hash(
                        workspace_id,
                        idempotency_hash,
                    )
                )

                if duplicate_by_key is not None:
                    self._assert_workspace_access(
                        duplicate_by_key,
                        context,
                    )
                    return self._safe_result(
                        "Existing payment request returned for the same "
                        "idempotency key.",
                        data={
                            "request": duplicate_by_key.to_dict(),
                            "duplicate": True,
                            "payment_executed": False,
                        },
                        metadata={
                            "idempotent_replay": True,
                            "runtime_ms": self._elapsed_ms(started_at),
                        },
                    )

            request = PaymentRequest(
                request_id=_new_id("payment_request"),
                user_id=context["user_id"],
                workspace_id=context["workspace_id"],
                actor_role=context["role"],
                action=normalized_action,
                amount=clean_amount,
                currency=clean_currency,
                purpose=clean_purpose,
                recipient=clean_recipient,
                status=PaymentRequestStatus.PENDING_REVIEW.value,
                decision=PaymentDecision.REQUIRE_APPROVAL.value,
                required_approvals=(
                    self.config.default_required_approvals
                ),
                idempotency_key_hash=idempotency_hash,
                payment_method_reference=self._validate_reference(
                    payment_method_reference,
                    "payment_method_reference",
                ),
                source_account_reference=self._validate_reference(
                    source_account_reference,
                    "source_account_reference",
                ),
                destination_reference=self._validate_reference(
                    destination_reference,
                    "destination_reference",
                ),
                merchant_reference=self._validate_reference(
                    merchant_reference,
                    "merchant_reference",
                ),
                invoice_reference=self._validate_reference(
                    invoice_reference,
                    "invoice_reference",
                ),
                subscription_reference=self._validate_reference(
                    subscription_reference,
                    "subscription_reference",
                ),
                project_id=self._validate_optional_id(
                    project_id,
                    "project_id",
                ),
                client_id=self._validate_optional_id(
                    client_id,
                    "client_id",
                ),
                task_id=self._validate_optional_id(
                    task_id,
                    "task_id",
                ),
                device_id=self._validate_optional_id(
                    device_id,
                    "device_id",
                ),
                session_id=self._validate_optional_id(
                    session_id,
                    "session_id",
                ),
                ip_hash=(
                    _hash_text(
                        f"{workspace_id}:{_clean_string(ip_address)}"
                    )
                    if ip_address
                    else None
                ),
                country_code=clean_country,
                metadata=_redact_sensitive_data(dict(metadata or {})),
                expires_at=(
                    _utc_now()
                    + timedelta(
                        seconds=self.config.request_ttl_seconds
                    )
                ).isoformat(),
            )

            risk_result = self._calculate_risk(
                request=request,
                context=context,
            )

            request.risk_score = risk_result["risk_score"]
            request.risk_level = risk_result["risk_level"]
            request.risk_findings = risk_result["findings"]
            request.required_approvals = risk_result[
                "required_approvals"
            ]
            request.decision = risk_result["decision"]
            request.status = risk_result["status"]
            request.updated_at = _utc_now_iso()

            security_review = self._request_security_approval(
                normalized_action,
                context,
                {
                    "request": request.to_dict(),
                    "risk": risk_result,
                },
            )

            if not security_review.get("approved", False):
                request.status = PaymentRequestStatus.HELD.value
                request.decision = PaymentDecision.HOLD.value
                request.risk_findings.append(
                    RiskFinding(
                        code="SECURITY_AGENT_HOLD",
                        message=_clean_string(
                            security_review.get("reason"),
                            "Security Agent held this request.",
                        ),
                        score=20,
                        severity=RiskLevel.HIGH.value,
                        blocking=True,
                    ).to_dict()
                )

            if (
                request.risk_level == RiskLevel.CRITICAL.value
                and self.config.require_security_client_for_critical
                and security_review.get("fallback", False)
            ):
                request.status = PaymentRequestStatus.HELD.value
                request.decision = PaymentDecision.HOLD.value
                request.risk_findings.append(
                    RiskFinding(
                        code="CRITICAL_REQUIRES_SECURITY_SERVICE",
                        message=(
                            "Critical financial risk requires an available "
                            "Security Agent service."
                        ),
                        score=20,
                        severity=RiskLevel.CRITICAL.value,
                        blocking=True,
                    ).to_dict()
                )

            with self._mutation_lock:
                stored = self.storage.create(request)

            verification_payload = self._prepare_verification_payload(
                normalized_action,
                context,
                stored.to_dict(),
                success=True,
            )
            memory_payload = self._prepare_memory_payload(
                stored,
                "payment_request_created",
            )

            self._emit_agent_event(
                "payment_guard.request_created",
                context,
                {
                    "request_id": stored.request_id,
                    "status": stored.status,
                    "decision": stored.decision,
                    "risk_level": stored.risk_level,
                },
            )
            self._log_audit_event(
                normalized_action,
                context,
                request_id=stored.request_id,
                success=True,
                metadata={
                    "status": stored.status,
                    "decision": stored.decision,
                    "risk_score": stored.risk_score,
                    "risk_level": stored.risk_level,
                },
            )

            return self._safe_result(
                self._creation_message(stored),
                data={
                    "request": stored.to_dict(),
                    "security_review": _redact_sensitive_data(
                        security_review
                    ),
                    "verification_payload": verification_payload,
                    "memory_payload": memory_payload,
                    "payment_executed": False,
                    "next_step": self._next_step(stored),
                },
                metadata={
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

        except Exception as exc:
            logger.exception("Failed to create protected payment request.")

            if context:
                self._log_audit_event(
                    normalized_action or "create_payment_request",
                    context,
                    success=False,
                    metadata={"error": str(exc)},
                )

            return self._error_result(
                "Failed to create protected payment request.",
                error=exc,
                metadata={
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

    # =========================================================================
    # Risk Assessment
    # =========================================================================

    def assess_payment_request(
        self,
        *,
        user_id: str,
        workspace_id: str,
        role: str,
        request_id: str,
        session_id: Optional[str] = None,
        device_id: Optional[str] = None,
    ) -> Dict[str, Any]:
        """Reassess a stored request without executing it."""
        started_at = time.perf_counter()
        action = PaymentAction.REVIEW.value

        try:
            context = self._validate_task_context(
                user_id,
                workspace_id,
                role,
                action,
                session_id=session_id,
                device_id=device_id,
            )
            self._check_permission(context, action)

            request = self._get_request_or_raise(request_id)
            self._assert_workspace_access(request, context)
            self._ensure_not_terminal(request)

            risk_result = self._calculate_risk(request, context)

            updates = {
                "risk_score": risk_result["risk_score"],
                "risk_level": risk_result["risk_level"],
                "risk_findings": risk_result["findings"],
                "required_approvals": risk_result[
                    "required_approvals"
                ],
                "decision": risk_result["decision"],
                "status": risk_result["status"],
                "updated_at": _utc_now_iso(),
                "version": request.version + 1,
            }

            with self._mutation_lock:
                updated = self.storage.update(
                    request.request_id,
                    updates,
                )

            if updated is None:
                raise PaymentStateError(
                    "Payment request disappeared during reassessment."
                )

            verification_payload = self._prepare_verification_payload(
                action,
                context,
                updated.to_dict(),
                success=True,
            )

            self._emit_agent_event(
                "payment_guard.request_reassessed",
                context,
                {
                    "request_id": updated.request_id,
                    "risk_level": updated.risk_level,
                    "status": updated.status,
                },
            )
            self._log_audit_event(
                action,
                context,
                request_id=updated.request_id,
                success=True,
                metadata={
                    "risk_score": updated.risk_score,
                    "risk_level": updated.risk_level,
                },
            )

            return self._safe_result(
                "Payment request reassessed. No payment was executed.",
                data={
                    "request": updated.to_dict(),
                    "verification_payload": verification_payload,
                    "payment_executed": False,
                },
                metadata={
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

        except Exception as exc:
            return self._error_result(
                "Payment request reassessment failed.",
                error=exc,
                metadata={
                    "request_id": request_id,
                    "runtime_ms": self._elapsed_ms(started_at),
                },
            )

    def _calculate_risk(
        self,
        request: PaymentRequest,
        context: Mapping[str, Any],
    ) -> Dict[str, Any]:
        """Calculate deterministic and optional external financial risk."""
        findings: List[RiskFinding] = []
        amount = request.amount
        action = request.action

        if amount >= self.config.critical_value_threshold:
            findings.append(
                RiskFinding(
                    code="CRITICAL_VALUE",
                    message="Amount meets the critical-value threshold.",
                    score=35,
                    severity=RiskLevel.CRITICAL.value,
                )
            )
        elif amount >= self.config.high_value_threshold:
            findings.append(
                RiskFinding(
                    code="HIGH_VALUE",
                    message="Amount meets the high-value threshold.",
                    score=20,
                    severity=RiskLevel.HIGH.value,
                )
            )

        if action in HIGH_RISK_ACTIONS:
            findings.append(
                RiskFinding(
                    code="HIGH_RISK_ACTION",
                    message=(
                        f"Financial action '{action}' has elevated risk."
                    ),
                    score=20,
                    severity=RiskLevel.HIGH.value,
                )
            )

        if action in IRREVERSIBLE_ACTIONS:
            findings.append(
                RiskFinding(
                    code="IRREVERSIBLE_ACTION",
                    message=(
                        "This financial action may be difficult or impossible "
                        "to reverse."
                    ),
                    score=15,
                    severity=RiskLevel.HIGH.value,
                )
            )

        if request.recipient.is_new_recipient:
            findings.append(
                RiskFinding(
                    code="NEW_RECIPIENT",
                    message="Recipient has not been used before.",
                    score=15,
                    severity=RiskLevel.MEDIUM.value,
                )
            )

        if not request.recipient.is_verified:
            findings.append(
                RiskFinding(
                    code="UNVERIFIED_RECIPIENT",
                    message="Recipient is not marked as verified.",
                    score=15,
                    severity=RiskLevel.HIGH.value,
                )
            )

        purpose_lower = request.purpose.lower()

        for term in SOCIAL_ENGINEERING_TERMS:
            if term in purpose_lower:
                findings.append(
                    RiskFinding(
                        code="SOCIAL_ENGINEERING_SIGNAL",
                        message=(
                            "Payment description contains an urgency or "
                            "approval-bypass signal."
                        ),
                        score=40,
                        severity=RiskLevel.CRITICAL.value,
                        blocking=True,
                    )
                )
                break

        duplicate = self._find_probable_duplicate(request)
        if duplicate is not None:
            findings.append(
                RiskFinding(
                    code="POSSIBLE_DUPLICATE",
                    message=(
                        "A similar recent payment request already exists."
                    ),
                    score=30,
                    severity=RiskLevel.HIGH.value,
                    blocking=True,
                    metadata={
                        "similar_request_id": duplicate.request_id,
                        "similar_status": duplicate.status,
                    },
                )
            )

        spending_findings = self._check_spending_limits(request)
        findings.extend(spending_findings)

        external_findings = self._collect_external_risk_findings(
            request,
            context,
        )
        findings.extend(external_findings)

        risk_score = min(
            100,
            max(0, sum(max(0, finding.score) for finding in findings)),
        )

        if risk_score >= 75:
            risk_level = RiskLevel.CRITICAL.value
        elif risk_score >= 45:
            risk_level = RiskLevel.HIGH.value
        elif risk_score >= 20:
            risk_level = RiskLevel.MEDIUM.value
        else:
            risk_level = RiskLevel.LOW.value

        blocking = any(finding.blocking for finding in findings)

        if blocking or risk_level == RiskLevel.CRITICAL.value:
            decision = PaymentDecision.HOLD.value
            status = PaymentRequestStatus.HELD.value
        else:
            decision = PaymentDecision.REQUIRE_APPROVAL.value
            status = PaymentRequestStatus.PENDING_APPROVAL.value

        if risk_level == RiskLevel.CRITICAL.value:
            required_approvals = max(
                self.config.critical_risk_required_approvals,
                self.config.default_required_approvals,
            )
        elif risk_level == RiskLevel.HIGH.value:
            required_approvals = max(
                self.config.high_risk_required_approvals,
                self.config.default_required_approvals,
            )
        else:
            required_approvals = (
                self.config.default_required_approvals
            )

        return {
            "risk_score": risk_score,
            "risk_level": risk_level,
            "findings": [
                finding.to_dict()
                for finding in findings
            ],
            "decision": decision,
            "status": status,
            "required_approvals": required_approvals,
            "payment_executed": False,
        }

    def _find_probable_duplicate(
        self,
        request: PaymentRequest,
    ) -> Optional[PaymentRequest]:
        """Find a same-workspace recent payment with matching core fields."""
        cutoff = _utc_now() - timedelta(
            seconds=self.config.duplicate_window_seconds
        )

        recipient_fingerprint = self._recipient_fingerprint(
            request.recipient
        )

        terminal_non_duplicate_statuses = {
            PaymentRequestStatus.REJECTED.value,
            PaymentRequestStatus.CANCELLED.value,
            PaymentRequestStatus.EXPIRED.value,
            PaymentRequestStatus.FAILED_REPORTED.value,
        }

        for existing in self.storage.list_by_workspace(
            request.workspace_id
        ):
            if existing.request_id == request.request_id:
                continue

            if existing.status in terminal_non_duplicate_statuses:
                continue

            created_at = _parse_iso_datetime(existing.created_at)
            if created_at is None or created_at < cutoff:
                continue

            if existing.action != request.action:
                continue

            if existing.amount != request.amount:
                continue

            if existing.currency != request.currency:
                continue

            if (
                self._recipient_fingerprint(existing.recipient)
                != recipient_fingerprint
            ):
                continue

            return existing

        return None

    def _check_spending_limits(
        self,
        request: PaymentRequest,
    ) -> List[RiskFinding]:
        """Enforce configurable single, daily, and monthly workspace limits."""
        limits = self.workspace_limits.get(
            request.workspace_id,
            {
                "single_limit": self.config.max_single_amount,
                "daily_limit": self.config.default_daily_limit,
                "monthly_limit": self.config.default_monthly_limit,
            },
        )

        findings: List[RiskFinding] = []

        if request.amount > limits["single_limit"]:
            findings.append(
                RiskFinding(
                    code="SINGLE_LIMIT_EXCEEDED",
                    message="Workspace single-payment limit exceeded.",
                    score=50,
                    severity=RiskLevel.CRITICAL.value,
                    blocking=True,
                )
            )

        now = _utc_now()
        day_start = datetime(
            now.year,
            now.month,
            now.day,
            tzinfo=timezone.utc,
        )
        month_start = datetime(
            now.year,
            now.month,
            1,
            tzinfo=timezone.utc,
        )

        countable_statuses = {
            PaymentRequestStatus.PENDING_APPROVAL.value,
            PaymentRequestStatus.APPROVED_FOR_EXTERNAL_EXECUTION.value,
            PaymentRequestStatus.EXECUTION_REPORTED.value,
            PaymentRequestStatus.HELD.value,
        }

        daily_total = ZERO_MONEY
        monthly_total = ZERO_MONEY

        for existing in self.storage.list_by_workspace(
            request.workspace_id
        ):
            if existing.request_id == request.request_id:
                continue

            if existing.currency != request.currency:
                continue

            if existing.status not in countable_statuses:
                continue

            created_at = _parse_iso_datetime(existing.created_at)
            if created_at is None:
                continue

            if created_at >= month_start:
                monthly_total += existing.amount

            if created_at >= day_start:
                daily_total += existing.amount

        proposed_daily = daily_total + request.amount
        proposed_monthly = monthly_total + request.amount

        if proposed_daily > limits["daily_limit"]:
            findings.append(
                RiskFinding(
                    code="DAILY_LIMIT_EXCEEDED",
                    message="Workspace daily payment limit exceeded.",
                    score=50,
                    severity=RiskLevel.CRITICAL.value,
                    blocking=True,
                    metadata={
                        "currency": request.currency,
                        "proposed_total": format(
                            proposed_daily,
                            "f",
                        ),
                        "limit": format(
                            limits["daily_limit"],
                            "f",
                        ),
                    },
                )
            )

        if proposed_monthly > limits["monthly_limit"]:
            findings.append(
                RiskFinding(
                    code="MONTHLY_LIMIT_EXCEEDED",
                    message="Workspace monthly payment limit exceeded.",
                    score=50,
                    severity=RiskLevel.CRITICAL.value,
                    blocking=True,
                    metadata={
                        "currency": request.currency,
                        "proposed_total": format(
                            proposed_monthly,
                            "f",
                        ),
                        "limit": format(
                            limits["monthly_limit"],
                            "f",
                        ),
                    },
                )
            )

        return findings

    def _collect_external_risk_findings(
        self,
        request: PaymentRequest,
        context: Mapping[str, Any],
    ) -> List[RiskFinding]:
        """Safely consume optional risk, fraud, and anomaly services."""
        findings: List[RiskFinding] = []

        payload = {
            "request": request.to_dict(),
            "context": _redact_sensitive_data(dict(context)),
            "non_execution": True,
        }

        clients: Tuple[Tuple[str, Optional[Any], Tuple[str, ...]], ...] = (
            (
                "risk_engine",
                self.risk_client,
                ("assess_payment", "assess", "evaluate", "score"),
            ),
            (
                "fraud_detector",
                self.fraud_client,
                ("detect_payment_fraud", "detect", "evaluate"),
            ),
            (
                "anomaly_detector",
                self.anomaly_client,
                ("detect_payment_anomaly", "detect", "evaluate"),
            ),
        )

        for source, client, methods in clients:
            if client is None:
                continue

            try:
                response = self._call_optional_client_sync(
                    client,
                    methods,
                    payload,
                )
            except Exception as exc:
                findings.append(
                    RiskFinding(
                        code=f"{source.upper()}_UNAVAILABLE",
                        message=(
                            f"{source} failed safely and requires "
                            "additional review."
                        ),
                        score=10,
                        severity=RiskLevel.MEDIUM.value,
                        metadata={"error": str(exc)},
                    )
                )
                continue

            findings.extend(
                self._normalize_external_findings(
                    source,
                    response,
                )
            )

        return findings

    def _normalize_external_findings(
        self,
        source: str,
        response: Any,
    ) -> List[RiskFinding]:
        """Normalize optional security service responses."""
        if response is None:
            return []

        output: List[RiskFinding] = []

        if isinstance(response, Mapping):
            raw_findings = response.get("findings")

            if isinstance(raw_findings, Sequence) and not isinstance(
                raw_findings,
                (str, bytes),
            ):
                for item in raw_findings:
                    if not isinstance(item, Mapping):
                        continue
                    output.append(
                        RiskFinding(
                            code=_clean_string(
                                item.get("code"),
                                f"{source.upper()}_FINDING",
                            ),
                            message=_clean_string(
                                item.get("message"),
                                f"{source} reported a risk signal.",
                            ),
                            score=self._clamp_score(
                                item.get("score", 10)
                            ),
                            severity=self._normalize_risk_level(
                                item.get("severity")
                                or item.get("risk_level")
                            ),
                            blocking=bool(
                                item.get(
                                    "blocking",
                                    item.get("blocked", False),
                                )
                            ),
                            metadata=_redact_sensitive_data(
                                dict(item.get("metadata") or {})
                            ),
                        )
                    )

            elif bool(
                response.get("fraud")
                or response.get("anomaly")
                or response.get("blocked")
                or response.get("high_risk")
            ):
                output.append(
                    RiskFinding(
                        code=f"{source.upper()}_RISK",
                        message=_clean_string(
                            response.get("message")
                            or response.get("reason"),
                            f"{source} reported elevated risk.",
                        ),
                        score=self._clamp_score(
                            response.get("score", 30)
                        ),
                        severity=self._normalize_risk_level(
                            response.get("risk_level")
                            or response.get("severity")
                            or RiskLevel.HIGH.value
                        ),
                        blocking=bool(response.get("blocked", False)),
                        metadata={
                            "source": source,
                        },
                    )
                )

        elif response is False:
            output.append(
                RiskFinding(
                    code=f"{source.upper()}_DENIED",
                    message=f"{source} denied the request.",
                    score=40,
                    severity=RiskLevel.HIGH.value,
                    blocking=True,
                )
            )

        return output

