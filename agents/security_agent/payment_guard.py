class InMemoryPaymentRequestStore:
    """
    Thread-safe default store for tests and development.

    Production deployments should inject a persistent tenant-aware store.
    """

    def __init__(self) -> None:
        self._records: Dict[str, PaymentRequest] = {}
        self._lock = threading.RLock()

    def create(self, request: PaymentRequest) -> PaymentRequest:
        """Create an isolated copy of a request."""
        with self._lock:
            if request.request_id in self._records:
                raise PaymentValidationError(
                    "A payment request with this ID already exists."
                )
            self._records[request.request_id] = copy.deepcopy(request)
            return copy.deepcopy(request)

    def get(self, request_id: str) -> Optional[PaymentRequest]:
        """Get a protected copy."""
        with self._lock:
            request = self._records.get(request_id)
            return copy.deepcopy(request) if request else None

    def update(
        self,
        request_id: str,
        updates: Mapping[str, Any],
    ) -> Optional[PaymentRequest]:
        """Update permitted dataclass attributes."""
        with self._lock:
            request = self._records.get(request_id)
            if request is None:
                return None

            for key, value in updates.items():
                if not hasattr(request, key):
                    continue
                setattr(request, key, copy.deepcopy(value))

            self._records[request_id] = request
            return copy.deepcopy(request)

    def list_by_workspace(
        self,
        workspace_id: str,
    ) -> List[PaymentRequest]:
        """List only records belonging to one workspace."""
        with self._lock:
            return [
                copy.deepcopy(request)
                for request in self._records.values()
                if request.workspace_id == workspace_id
            ]

    def find_by_idempotency_hash(
        self,
        workspace_id: str,
        idempotency_key_hash: str,
    ) -> Optional[PaymentRequest]:
        """Find a same-workspace request by one-way idempotency hash."""
        with self._lock:
            for request in self._records.values():
                if (
                    request.workspace_id == workspace_id
                    and request.idempotency_key_hash == idempotency_key_hash
                ):
                    return copy.deepcopy(request)
        return None


