APPROVAL_GATED = "Persisted only as part of an approval-request record; sensitive payload fields redacted until resolved."
EPHEMERAL = "Held only for the lifetime of the active session/request; not persisted to durable storage."

SCHEMA_CHECK = "VerificationAgent confirms the response matches the normalized result schema."
AUDIT_CHECK = "VerificationAgent confirms a matching audit log row was written for this decision."
APPROVAL_CHECK = "VerificationAgent confirms an approval-request record exists with the expected resolved state."
DB_CHECK = "VerificationAgent confirms the expected row exists/changed in the scoped DB table for this action."
UNAVAILABLE_CHECK = "N/A while capability_status is external_dependency_required or planned; verification is skipped and surfaced as such."

