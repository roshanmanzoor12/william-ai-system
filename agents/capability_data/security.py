"""Capability data for the Security Agent (agent_key="security").

Purpose (from mission spec): Risk, approval, policy, audit, fraud, anomaly,
and permission brain.

Live MVP behavior:
- Classify/risk-score/approve/deny/log.
- Block critical actions by default.
- No risky action proceeds without approval.
"""

from __future__ import annotations

